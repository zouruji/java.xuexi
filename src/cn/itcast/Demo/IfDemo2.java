package cn.itcast.Demo;

public class IfDemo2 {
    public static void main(String[] args) {
        //创建变量
        int a = 10;
        int b = 20;
        //判断程序是否相等,相等则执行第一个语句!不相等则执行第二个语句
        if( a == b) {
            System.out.println("相等");
        }else {
            System.out.println("不相等");
        }
    }
}
