package no.Sprace.Demo;

/*
 算数运算符:
 分类: * , - , +, / , %
 /和%的区别:
 /:获取两个数据相除的商
 %:获取两个数据相除的余数
 注意:
 整数相除结果还是整数
 想要得到小数,就必须有浮点数参与话题
 */


//定义两个int 类型的变量
public class OperatorDemo {
    public static void main(String[] args) {
        int a = 4;
        int b = 5;
        System.out.println(a + b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

    }
}
