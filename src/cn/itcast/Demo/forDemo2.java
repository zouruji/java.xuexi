
package cn.itcast.Demo;

public class forDemo2 {
    public static void main(String[] args) {
        //需求:打印1-5的数字
        for(int a = 1 ; a  <= 5; a ++){
            System.out.println(a);
        }
        System.out.println("----------------------------------");
        //需求:打印5-1之间的数字
        for(int a = 5 ; a >=1 ; a --){
            System.out.println(a);
        }
    }
}
